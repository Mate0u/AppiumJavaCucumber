describe('Filling feedback form', () => {
	it('Fill feedback form and send message', () => {
		browser.url('http://zero.webappsecurity.com/')
		const feedbackButton = browser.$('#feedback')
		feedbackButton.waitForExist()
		feedbackButton.click()
		const headderFeedback = browser.$('#feedback-title')
		headderFeedback.waitForExist()
		const nameField = browser.$('#name')
		nameField.waitForExist()
		nameField.setValue('Han')
		const addresseField = browser.$('#email')
		addresseField.waitForExist()
		addresseField.setValue('Han@solo.com')
		const subjectField = browser.$('#subject')
		subjectField.waitForExist()
		subjectField.setValue('Test subject')
		const commentField = browser.$('#comment')
		commentField.waitForExist()
		commentField.setValue('Lorem ipsum')
		browser.saveScreenshot('./test/screenshots/feedbackForm.png')
		const sendButton = browser.$('input[name=submit]')
		sendButton.waitForExist()
		sendButton.click()
		expect(browser).toHaveUrl(
			'http://zero.webappsecurity.com/sendFeedback.html'
		)
		// const h3Title = browser.$('#feedback-title')
		headderFeedback.waitForExist()
	})
})
